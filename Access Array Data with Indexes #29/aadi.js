/* Access Array Data with Indexes */

/*
Elements in an array start counting from 0

Example:
In the variable below 'ourArray' the '50' (First value/element of the array) is index 0, second value '60' (Second value/element of the array) is index 1, and third value '70' (Third value/element of the array) is index 2.

var anArray = [20, 60, 90];
                0   1   2
*/

var ourArray = [50, 60, 70]; // Current values/elements in 'ourArray' variable is: 50 | 60 | 70
var ourData = ourArray[0]; // Current value of 'ourData' variable is: 50

var myArray = [50, 60, 70]; // Current values/elements in 'myArray' variable is: 50 | 60 | 70
var myData = myArray[0]; // Current value of 'myData' variable is: 50