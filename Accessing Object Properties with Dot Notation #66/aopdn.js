/* Accessing Object Properties with Dot Notation */

var testObj = {
    "hat": "ballcap", // Property 'hat' with a value of: ballcap
    "shirt": "jersey", // Property 'shirt' with a value of: jersey
    "shoes": "cleats" // Property 'shoes' with a value of: cleats
};

var hatValue = testObj.hat; // Current value of the 'hatValue' variable is: ballcap (Because the 'testObj' variable has the property of 'hat' and the 'hat' value is 'ballcap' so you get: ballcap)
var shirtValue = testObj.shirt; // Current value of the 'shirtValue' variable is: jersey (Because the 'testObj' variable has the property of 'shirt' and the 'shirt' value is 'jersey' so you get: jersey)

// You can access properties in an object with the "." notation: [objectName].[propertyName]