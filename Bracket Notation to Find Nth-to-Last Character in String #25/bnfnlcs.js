/** Bracket Notation to Find Nth-to-Last Character in String */

var firstName = "Ada"; // Current value of 'firstName' is: Ada (Type: String)
var thirdToLastLetterOfFirstName = firstName[firstName.length - 3]; // Current value of 'thirdToLastLetterOfFirstName' is: A (Type: String)

var lastName = "Lovelace"; // Current value of 'lastName' is: Lovelace (Type: String)
var secondToLastLetterOfLastName = lastName[lastName.length - 2]; // Current value of 'secondToLastLetterOfLastName' is: c (Type: String)