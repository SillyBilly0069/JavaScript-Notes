/* Concatenating Strings with Plus Operator */

var ourStr = "I come first. " + "I come second."; // Current value of 'ourStr' is: I come first. I come second. (Type: String) 

var myStr = "This is the start. " + "This is the end."; // Current value of 'myStr' is: This is the start. This is the end. (Type: String)