/* Concatenating Strings with Plus Equals Operator */

var ourStr = "I come first. "; // Current value of 'ourStr' is: I come first.[Space] (Type: String)
ourStr += "I come second."; // Current value of 'ourStr' is: I come first. I come second. (Type: String)

var myStr = "This is the first sentence. "; // Current value of 'myStr' is: This is the first sentence.[Space] (Type: String)
myStr += "This is the second sentence."; // Current value of 'myStr' is: This is the first sentence. This is the second sentence. (Type: String)