/* Boolean Values */

/* Booleans are another type of data types in JavaScript, there are only 2 values: true/false (Think of it as on/off switches, or yes/no)*/

function welcomeToBooleans() {
    return false; // Returns the 'false' value (Type: Boolean)
}

console.log(welcomeToBooleans()); // Prints out to the browser console: false (Because the return value in the 'welcomeToBooleans' function is: false)


function welcomeToBooleans2() {
    return true; // Returns the 'true' value (Type: Boolean)
}

console.log(welcomeToBooleans2()); // Prints out to the browser console: true (Because the return value in the 'welcomeToBooleans2' function is: true)
