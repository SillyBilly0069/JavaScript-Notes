/* Case Sensitivity in Variables */


/* 
Variable names and function names in JavaScript are Case Sensitive, that means that capitilization matters

Example:
currentValue is not the same as currentvalue | The letters are the same, but the capitilization is not
*/


// Declarations
var studlyCapVar; // Current value is: undefined/null
var properCamelCase; // Current value is: undefined/null
var titleCaseOver; // Current value is: undefined/null


// Assignments
studlyCapVar = 10; // Current value is: 10
properCamelCase = "A String"; // Current value is: "A String"
titleCaseOver = 9000; // Current value is: 9000