// The remainder operator looks like a % (Percent) sign. And it gives the remainder of the division of two numbers


var remainder; // Current value is: undefined/null
remainder = 11 % 3; // Current value is: 2 (That's because '11 / 3 = 9' and then '11 - 9 = 2' which makes the remainder be 2.)

