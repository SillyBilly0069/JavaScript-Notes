/* Write Reusable Code with Functions */

// Functions allow us to use reusable code


/*
Syntax Info:

Keyword  |    Function Name    | parameters => (parameter1, parameter2) | {Code Inside Curly Brackets}
function     functionNameHere                      ()                                 {}

Syntax:

function functionNameHere() {
    // Code
}
*/


function ourReusableFunction() {
    console.log("Heyya, World"); // Prints on the browser console: Heyya, World (Type: String)
}

ourReusableFunction(); // Calls/Executes the function code

function reusableFunction() {
    console.log("Hi World"); // Prints on the browser console: Hi World (Type: String)
}

reusableFunction(); // Calls/Executes the function code